# Ввод начального количества амеб от пользователя
amoeba_count = int(input("Введите начальное количество амеб: "))

# Инициализация переменных
time = 3  # Время в часах
while time <= 24:
    print(f"Через {time} часа(ов) будет {amoeba_count} амёб")

    # Каждые 3 часа амёбы делятся на 2
    amoeba_count *= 2

    time += 3
