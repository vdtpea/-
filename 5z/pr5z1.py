# Ввод числа от пользователя
num = float(input("Введите число (для завершения введите 0): "))

# Цикл с условием
while num != 0:
    # Вывод обратного числа
    print("Обратное число:", 1 / num)

    # Ввод следующего числа
    num = float(input("Введите число (для завершения введите 0): "))
