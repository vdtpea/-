# Запрос ввода непустой последовательности символов у пользователя
sequence = input("Введите непустую последовательность символов: ")

# Построение множества из встречающихся цифр от '0' до '9'
digits_set = set(char for char in sequence if char.isdigit())

# Вывод множества цифр
print("Множество цифр от '0' до '9':", digits_set)
