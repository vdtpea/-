# Запрос ввода непустой последовательности символов у пользователя
sequence = input("Введите непустую последовательность символов: ")

# Построение множества из встречающихся символов от '5' до '9' и знаков арифметических операций
desired_set = set(char for char in sequence if char.isdigit() and '5' <= char <= '9' or char in '+-*/')

# Вывод множества
print("Множество символов от '5' до '9' и знаков арифметических операций:", desired_set)
