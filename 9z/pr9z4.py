# Запрос ввода строки у пользователя
input_string = input("Введите строку: ")

# Создание строки, в которой каждый символ повторяется дважды
doubled_string = ''.join(char * 2 for char in input_string)

# Вывод результирующей строки
print("Строка с каждым символом, повторенным дважды:", doubled_string)
