# Запрос ввода двух чисел
x = float(input("Введите число x: "))
y = float(input("Введите число y: "))

# Проверка условия и изменение чисел x и y
if x < y:
    x = (x + y) / 2
    y = 2 * x * y
else:
    y = (x + y) / 2
    x = 2 * x * y

# Вывод значений переменных
print(f"Измененное число x: {x}")
print(f"Измененное число y: {y}")
