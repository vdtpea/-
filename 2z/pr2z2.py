import math

# Ввод исходных данных с клавиатуры
x = float(input("Введите значение х: "))
y = float(input("Введите значение у: "))

# Вычисление выражения
b = x**(x/y) - math.pow(y/x, 1/3)

# Вывод результатов
print ("Исходные данные:")
print("x =", x)
print("y =", y)
print("Результат вычисления:")
print("b =", b)

#Вывод целой части результата
print("Целая часть результата:", math.floor(b))